"""P8: Regulatory & Accreditation Automation API routes."""
from __future__ import annotations

from fastapi import APIRouter, Depends, Query, Request
from fastapi.responses import Response
from sqlalchemy.orm import Session

from app.deps import get_db
from app.enterprise_auth import require_enterprise_auth
from app.schemas.regulatory import AuditPackageRequest, FDASubmissionCreate
from app.services.accreditation_engine import (
    compute_accreditation_readiness,
    compute_regulatory_dashboard,
    generate_audit_package,
    list_fda_submissions,
    map_finding_to_clauses,
)
from app.tier_guard import require_tier

router = APIRouter(prefix="/api/regulatory", tags=["regulatory"])


@router.get("/readiness")
def accreditation_readiness(
    request: Request,
    tenant_id: str = Query(...),
    facility_id: str = Query(default=""),
    db: Session = Depends(get_db),
):
    """Real-time accreditation readiness score across all regulatory bodies."""
    require_enterprise_auth(request)
    require_tier(tenant_id, "regulatory_readiness", db)
    result = compute_accreditation_readiness(tenant_id, facility_id, db)
    return {"status": "success", "readiness": result.model_dump()}


@router.get("/readiness/findings")
def readiness_findings(
    request: Request,
    tenant_id: str = Query(...),
    facility_id: str = Query(default=""),
    severity: str = Query(default=""),
    db: Session = Depends(get_db),
):
    """List all accreditation deficiency findings, optionally filtered by severity."""
    require_enterprise_auth(request)
    require_tier(tenant_id, "regulatory_findings", db)
    result = compute_accreditation_readiness(tenant_id, facility_id, db)
    findings = result.findings
    if severity:
        findings = [f for f in findings if f.severity == severity]
    return {
        "status": "success",
        "total": len(findings),
        "findings": [f.model_dump() for f in findings],
    }


@router.get("/standards")
def list_standards(
    request: Request,
    body: str = Query(default=""),
    category: str = Query(default=""),
):
    """List all regulatory standards in the catalogue."""
    require_enterprise_auth(request)
    from app.services.regulatory_standards_catalogue import get_standards
    stds = get_standards()
    if body:
        stds = [s for s in stds if s.body == body]
    if category:
        stds = [s for s in stds if s.category == category]
    return {
        "status": "success",
        "standards": [
            {"code": s.code, "body": s.body, "title": s.title,
             "description": s.description, "category": s.category}
            for s in stds
        ],
    }


@router.get("/standards/mapping")
def finding_clause_mapping(
    request: Request,
    finding_category: str = Query(...),
    severity: str = Query(default="any"),
):
    """Map a CV finding category to triggered regulatory clauses."""
